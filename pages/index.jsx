import Head from "next/head";

import connectDB from "../lib/dbConnect";
import Muestra from "./models/Muestra";

export default function Home({ muestras }) {
  console.log(muestras);
  return (
    <div>
      <Head>
        <title>DB</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>

      <main className="container">
        <h1>LAOF DataBase APP</h1>
        <h2>Muestras</h2>
        {muestras.map(
          ({ _id, marca_temporal, evaluador, nombre, numero_mandibula }) => (
            <div className="card mb-2" key={_id}>
              <div className="card-body">
                <div className="h4">ID: {numero_mandibula}</div>
                <p>Nombre: {nombre}</p>
                <p>Evaluador{evaluador}</p>
                <p>Marca temporal: {marca_temporal}</p>
              </div>
            </div>
          )
        )}
      </main>
    </div>
  );
}

export async function getServerSideProps() {

  await connectDB();

  const res = await Muestra.find({});

  console.log("Response:", res);

  const muestras = res.map((doc) => {
    const muestra = doc.toObject();
    muestra._id = `${muestra._id}`;
    return muestra;
  });

  return { props: { muestras } };

}